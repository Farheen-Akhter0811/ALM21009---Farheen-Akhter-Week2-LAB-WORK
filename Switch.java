package week2;

public class Switch {

	public static void main(String[] args) {
		String s = "Hello";
		switch(s) {
		case "Hi":
			System.out.println("I am String Hi!");
			break;
		case "Hello":
			System.out.println("I am String Hello!");
			break;
		default:
			System.out.println("I am default..");
		}

	}

}
